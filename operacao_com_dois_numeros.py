a = float(input("digite um numero: "))
b = float(input("Digite um numero: "))
c = input("Digite qual vai ser a operação: ")
if c == "+" :
    print (a+b)
if c == "-":
    print (a-b)
if c == "*":
    print (a*b)
if c == "/":
    print (a/b)